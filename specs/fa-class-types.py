
# class - a generic class. Objects are instatiated and referenced by pointers
# structure - as in Swift, a structure is similar to a class but instantiation does not create a pointer
# unnamed structures - are not referenced either
# abstract - Can't be instantiated. Can only be inherited
# unique - a unique is a singleton. Can't be instantiated. Very similar to an unnamed structure, with the difference it can inherit from other classes
# interface - used to interface a C++ or JS lib to some Fa code


